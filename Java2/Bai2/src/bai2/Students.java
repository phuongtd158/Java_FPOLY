package bai2;

/**
 *
 * @author ADMIN
 */
public class Students {
    public String name, id, nganh;

    public Students() {
    }

    public Students(String name, String id, String nganh) {
        this.name = name;
        this.id = id;
        this.nganh = nganh;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNganh() {
        return nganh;
    }

    public void setNganh(String nganh) {
        this.nganh = nganh;
    }
    
    
}
