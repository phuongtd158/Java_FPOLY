    public void add() {
        int index = tblShow.getSelectedRow();
        SinhVien1 sv = new SinhVien1();

        sv.setKyNang(cbbKyNang.getSelectedItem().toString());
        sv.setGioiTinh(rdoNam.isSelected() ? "Nam" : "Nữ");
        sv.setThieu(checkThieu());

        //Check mã sinh viên
        try {
            if (tfMaMeo.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Không được để trống mã sinh viên");
                return;
            } else {
                sv.setMaMeo(tfMaMeo.getText());
            }
        } catch (Exception e) {
            System.out.println("Lỗi");
        }

        //Check tên sinh viên
        try {
            if (tfTen.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Không được để trống tên sinh viên");
                return;
            } else {
                sv.setHoTen(tfTen.getText());
            }
        } catch (Exception e) {

        }

        //Check điểm
        try {
            if (tfDiem.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "Điểm ko trống");
                return;
            } else if (Double.parseDouble(tfDiem.getText()) < 0 || Double.parseDouble(tfDiem.getText()) > 10) {
                JOptionPane.showMessageDialog(this, "Diem lon hon 0 va nho hon 10");
                return;
            } else {
                sv.setDiem(Double.parseDouble(tfDiem.getText()));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Không đúng định dạng");
        }

        //Check độ dài tên
        try {
            if (tfTen.getText().length() > 40) {
                JOptionPane.showMessageDialog(this, "Tên phải nhỏ hơn 40 ký tự");
                return;
            }
        } catch (Exception e) {
        }

        //Check độ dài mã sinh viên
        try {
            if (tfMaMeo.getText().length() > 7) {
                JOptionPane.showMessageDialog(this, "Mã sinh viên phải nhỏ hơn 7 ký tự");
                return;
            }
        } catch (Exception e) {
        }

        //Check trùng tên
        try {
            if (checkTrung(tfTen.getText()) == true) {
                JOptionPane.showMessageDialog(this, "Trung ten");
                return;
            }
        } catch (Exception e) {
            System.out.println("Lỗi");
        }

        listSv.add(sv);

    }

