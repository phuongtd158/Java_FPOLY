    private void tblShowMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblShowMouseClicked
        int index = tblShow.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) tblShow.getModel();
        tfTen.setText(model.getValueAt(index, 0).toString());
        tfMaMeo.setText(model.getValueAt(index, 1).toString());
        tfDiem.setText(model.getValueAt(index, 2).toString());
        cbbKyNang.setSelectedItem(model.getValueAt(index, 3).toString());

        if (listSv.get(index).getGioiTinh().equals("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }

        if (listSv.get(index).getThieu().length() == 0) {
            chkThieuTien.setSelected(false);
            chkThieuTinh.setSelected(false);
        } else if (listSv.get(index).getThieu().equals("Thiếu tình")) {
            chkThieuTinh.setSelected(true);
            chkThieuTien.setSelected(false);
        } else if (listSv.get(index).getThieu().equals("Thiếu tiền")) {
            chkThieuTien.setSelected(true);
            chkThieuTinh.setSelected(false);
        } else {

            chkThieuTien.setSelected(true);
            chkThieuTinh.setSelected(true);
        }
    }//GEN-LAST:event_tblShowMouseClicked

