    public void fillToTable() {

        DefaultTableModel model = (DefaultTableModel) tblShow.getModel();
        model.setRowCount(0);

        for (SinhVien1 x : listSv) {
            model.addRow(new Object[]{
                x.maMeo, x.hoTen, x.diem, x.kyNang, x.gioiTinh, x.thieu
            });

        }
    }

    public void remove() {
        DefaultTableModel model = (DefaultTableModel) tblShow.getModel();
        int index = tblShow.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Thêm thông tin để xóa");
        } else {
            model.removeRow(index);
            JOptionPane.showMessageDialog(this, "Xóa thành công");
        }
    }

    public void update() {
        int index = tblShow.getSelectedRow();
        SinhVien1 sv = listSv.get(index);
        sv.hoTen = tfTen.getText();
        sv.diem = Double.parseDouble(tfDiem.getText());
        sv.maMeo = tfMaMeo.getText();
        sv.kyNang = (String) cbbKyNang.getSelectedItem();
        sv.gioiTinh = rdoNam.isSelected() ? "Nam" : "Nữ";
        sv.thieu = checkThieu();
    }

