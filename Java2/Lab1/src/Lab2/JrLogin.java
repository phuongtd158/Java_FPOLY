    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
       int index = tblUser.getSelectedRow();
       String newUser = txtUser.getText();
       String newPass = String.copyValueOf(txtPassword.getPassword());
       if (index == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần cập nhật !");
        } else if (listUser.size() == 0) {
            JOptionPane.showMessageDialog(this, "Không có thông tin để cập nhật");
        } else {
        listUser.get(index).setUser(txtUser.getText());
        listUser.get(index).setPassword(txtPassword.getText());
           if (chkAdmin.isSelected() == true) {
               listUser.get(index).setRole("Admin");
           }
           else listUser.get(index).setRole("User");
          JOptionPane.showMessageDialog(this, "Cập nhật thành công");
          fillToTable();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

