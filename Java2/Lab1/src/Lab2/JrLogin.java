    public void clear() {
        txtUser.setText("");
        txtPassword.setText("");
        
    }

    public boolean checkTrung(String newUser) {
        boolean check = false;
        for (User x : listUser) {
            if (x.getUser().equals(newUser)) {
                check = true;
            }
        }
        return check;
    }

    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        model.setRowCount(0);
        for (User x : listUser) {
            model.addRow(new Object[]{
                x.getUser(), x.getPassword(), x.getRole()
            });
        }
    }

    public void remove() {
        DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
        int index = tblUser.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn dòng cần xóa !");
        } else if (listUser.size() == 0) {
            JOptionPane.showMessageDialog(this, "Không có thông tin để xóa");
        } else {
            listUser.remove(index);
            fillToTable();
        }
    }

