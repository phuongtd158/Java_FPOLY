    private void dongHo() {
        Thread t = new Thread() {
            @Override
            public void run() {
                //Sau mỗi giây, lấy giờ hệ thống => hiển thị lên lblDongHo
                SimpleDateFormat sdf = new SimpleDateFormat("hh:mm:ss aa");
                while (true) {                    
                    Date d = new Date();
                    lbTime.setText(sdf.format(d));
                    try {
                        Thread.sleep(1000);
                    } catch (Exception e) {
                    }
                }
            }
            
        };
        t.start();
    }
    
