    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        if (txtSo1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Hãy quay số thứ 1");
            return;
        }
        else if (txtSo2.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Hãy quay số thứ 2");
            return;
        }
        else if (txtSo3.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Hãy quay số thứ 3");
            return;
        }
        else
        JOptionPane.showMessageDialog(this, "Kết quả của bạn là: " + txtSo1.getText() + txtSo2.getText() + txtSo3.getText());
    }//GEN-LAST:event_btnShowActionPerformed

