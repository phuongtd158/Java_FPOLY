    private void time() {
        t = new Thread() {
            @Override
            public void run() {
                SimpleDateFormat sdf = new SimpleDateFormat("hh:mm:ss aa");
                while (true) {
                    Date d = new Date();
                    lbTime.setText(sdf.format(d));
                    try {
                        Thread.sleep(10);
                    } catch (Exception e) {
                        break;
                    }
                }
            }
        };
        t.start();
    }

