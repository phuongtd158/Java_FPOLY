    public boolean check(String maMon) {

        try {
            cn = Prt_8.XHelper.ketNoi("TestAss1");
            String sql = "select MaMH from MONHOC";
            PreparedStatement stm = cn.prepareStatement(sql);
            ResultSet rs = stm.executeQuery();

            while (rs.next()) {
                if (rs.getString(1).equalsIgnoreCase(maMon)) {
                    return true;
                }
            }

            cn.close();
            stm.close();
            rs.close();
        } catch (Exception e) {
        }
        return false;
    }

    public void loadCbb() {
        try {
            cn = XHelper.ketNoi("TestAss1");
            String sql = "select MAMH from DIEM";

            PreparedStatement stm = cn.prepareStatement(sql);

            ResultSet rs = stm.executeQuery();

            while (rs.next()) {
                cbbMaMon.addItem(rs.getString(1));
            }

            cn.close();
            stm.close();
            rs.close();
        } catch (Exception e) {
        }
    }

