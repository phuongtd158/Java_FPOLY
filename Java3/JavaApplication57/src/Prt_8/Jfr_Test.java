    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            cn = XHelper.ketNoi("TestAss1");
            String sql = "insert into DIEM(Masv, MaMH, diem)\n"
                    + "values(?, ?, ?)";

            PreparedStatement statement = cn.prepareStatement(sql);

            statement.setString(1, txtMa.getText());
            if (check(cbbMaMon.getSelectedItem().toString()) == true) {
                JOptionPane.showMessageDialog(this, "trùng");
                return;
            } else {
                statement.setString(2, cbbMaMon.getSelectedItem().toString());
            }
            if (Double.parseDouble(txtDiem.getText()) < 0 || Double.parseDouble(txtDiem.getText()) > 10) {
                JOptionPane.showMessageDialog(this, "Điểm phải nhỏ hơn 10 và lớn hơn 0");
                return;
            } else {
                statement.setDouble(3, Double.parseDouble(txtDiem.getText()));
            }

            statement.executeUpdate();

            cn.close();
            statement.close();

            JOptionPane.showMessageDialog(this, "Thêm thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "trùng mã môn");
            return;
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            cn = XHelper.ketNoi("TestAss1");
            String sql = "update DIEM \n"
                    + "set diem = ? where MASV = ? and MAMH = ?";

            PreparedStatement statement = cn.prepareStatement(sql);

            statement.setString(2, txtMa.getText());
            statement.setString(3, (String) cbbMaMon.getSelectedItem());
            if (Double.parseDouble(txtDiem.getText()) < 0 || Double.parseDouble(txtDiem.getText()) > 10) {
                JOptionPane.showMessageDialog(this, "Điểm phải nhỏ hơn 10 và lớn hơn 0");
                return;
            } else {
                statement.setDouble(1, Double.parseDouble(txtDiem.getText()));
            }

            statement.executeUpdate();

            cn.close();
            statement.close();

            JOptionPane.showMessageDialog(this, "Cập nhật thành công");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

