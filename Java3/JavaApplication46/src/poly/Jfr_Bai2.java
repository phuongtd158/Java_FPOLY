    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String rs = "";
        try {
            if (check()) {

                rs = rs + "Họ" + jTextField1.getText() + "\n";
                rs = rs + "Tên" + jTextField2.getText() + "\n";
                rs = rs + "Bí danh " + jTextField3.getText() + "\n";
                rs = rs + "Mật mã" + jPasswordField1.getText() + "\n";
                if (jRadioButton1.isSelected()) {
                    rs = rs + "Giới tính " + jRadioButton1.getText() + "\n";
                } else if (jRadioButton2.isSelected()) {
                    rs = rs + "Giới tính " + jRadioButton2.getText() + "\n";
                } else {
                    rs = rs + "Giới tính " + jRadioButton3.getText() + "\n";
                }

            }
            rs = rs + "Quê quán " + jComboBox1.getSelectedItem();
            if (jCheckBox1.isSelected()) {
                rs = rs.equals("") ? rs + "Ăn chơi" : rs + ", Ăn chơi";
            } else if (jCheckBox2.isSelected()) {
                rs = rs.equals("") ? rs + "Chơi" : rs + ", Chơi";
            } else {
                rs = rs.equals("") ? rs + "Ngu" : rs + ", Ngủ";
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        JOptionPane.showMessageDialog(this, rs);
    }//GEN-LAST:event_jButton1ActionPerformed

    private boolean check() {
        if (jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Không được trống");
            return false;
        }
        return true;
    }

