    public void openThongKe(int index) {
        if (Auth.isLogin()) {
            if (index == 3 && !Auth.isManager()) {
                MsgBox.alert(this, "Bạn không có quyền xem doanh thu");
            } else {
                ThongKeJDialog tk = new ThongKeJDialog(this, true);
                tk.setVisible(true);
                tk.selectTabs(index);
            }
        } else {
            MsgBox.alert(this, "Chưa đăng nhập");
        }
    }

