    public boolean isDateValid(String date) {
        try {
            DateFormat df = new SimpleDateFormat("dd-MM-yyyy");
            df.setLenient(false);
            df.parse(date);
            return true;
        } catch (ParseException e) {
            return false;
        }
    }

    public void fillToTableByMaCD() {
        DefaultTableModel model = (DefaultTableModel) tblKhoaHoc.getModel();
        model.setRowCount(0);

        try {
            ChuyenDe cd = (ChuyenDe) cbbTenChuyenDe.getSelectedItem();
            List<KhoaHoc> list = dao.selectByChuyenDe(cd.getMaCD());
            for (KhoaHoc x : list) {
                model.addRow(new Object[]{x.getMaKH(), cd.getThoiLuong(), cd.getHocPhi(), x.getNgayKG(), x.getMaNV(), x.getNgayTao()});
                txtNguoiTao.setText(x.getMaNV());
                ((JTextField) txtNgayTao.getDateEditor().getUiComponent()).setText(XDate.toString(x.getNgayTao(), "dd-MM-yyyy"));
                ((JTextField) txtNgayKG.getDateEditor().getUiComponent()).setText(XDate.toString(x.getNgayKG(), "dd-MM-yyyy"));
            }
        } catch (Exception e) {
            e.printStackTrace();
            throw new RuntimeException();
        }

    }

    public void fillToComboBox() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbbTenChuyenDe.getModel();
        model.removeAllElements();
        try {
            List<ChuyenDe> list = cddao.selectAll();
            for (ChuyenDe x : list) {
                model.addElement(x);
            }
        } catch (Exception e) {
            e.printStackTrace();
            throw new RuntimeException();
        }

    }

    public void chonChuyenDe() {

        ChuyenDe cd = (ChuyenDe) cbbTenChuyenDe.getSelectedItem(); //Lấy ra tên chuyên đề

        txtThoiLuong.setText(cd.getThoiLuong() + "");
        txtTenCD.setText(cd.getTenCD());
        txtHocPhi.setText(cd.getHocPhi() + "");

        fillToTableByMaCD();
        row = -1;
        tabs.setSelectedIndex(1);
        updateStatus();
    }

    public void edit() {
        int maKh = (int) tblKhoaHoc.getValueAt(row, 0);
        try {
            KhoaHoc model_kh = dao.selectById(maKh);
            if (model_kh != null) {
                setForm(model_kh);
                tabs.setSelectedIndex(0);
                updateStatus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void setForm(KhoaHoc kh) {

        if (kh.getNgayKG() != null) {
            ((JTextField) txtNgayKG.getDateEditor().getUiComponent()).setText(XDate.toString(kh.getNgayKG(), "dd-MM-yyyy"));
        } else {
            ((JTextField) txtNgayKG.getDateEditor().getUiComponent()).setText("");
        }

        if (kh.getNgayTao() == null) {
            ((JTextField) txtNgayTao.getDateEditor().getUiComponent()).setText("");
        } else {
            ((JTextField) txtNgayTao.getDateEditor().getUiComponent()).setText(XDate.toString(kh.getNgayTao(), "dd-MM-yyyy"));
        }
        txtGhiChu.setText(kh.getGhiChu());
        txtNguoiTao.setText(kh.getMaNV());

    }

    public void updateStatus() {
        boolean edit = this.row >= 0;
        boolean first = this.row == 0;
        boolean last = this.row == tblKhoaHoc.getRowCount() - 1;

        txtTenCD.setEditable(!edit); //Nếu row >= 0 -> edit = true ->!edit = false

        btnThem.setEnabled(!edit); //row >=0 => !edit = false => ko duoc edit
        btnXoa.setEnabled(edit);
        btnSua.setEnabled(edit);

        btnF.setEnabled(!first && edit);
        btnP.setEnabled(!first && edit);
        btnN.setEnabled(!last && edit);
        btnL.setEnabled(!last && edit);
    }

    public void insert() {
        KhoaHoc kh = getForm();
        try {

            dao.insert(kh);
            fillToTableByMaCD();
            clear();
            MsgBox.alert(this, "Thêm thành công");

        } catch (Exception e) {
            MsgBox.alert(this, "Thêm thất bại");
            e.printStackTrace();
        }
    }

    public void update() {
        try {
            KhoaHoc kh = getForm();
            dao.update(kh);
            fillToTableByMaCD();
            MsgBox.alert(this, "Cập nhật thành công");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại");
            e.printStackTrace();
        }
    }

    public void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Chỉ trưởng phòng mới có quyền xóa");
        } else {
            try {
                int maKh = (int) tblKhoaHoc.getValueAt(row, 0);
                if (MsgBox.confirm(this, "Bạn có muốn xóa khóa học") == JOptionPane.YES_OPTION) {
                    dao.delete(maKh);
                    clear();
                    fillToTableByMaCD();

                    MsgBox.alert(this, "Xóa thành công");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

    public void clear() {
        KhoaHoc kh = new KhoaHoc();
        setForm(kh);
        row = -1;
        updateStatus();
    }

    KhoaHoc getForm() {

        ChuyenDe cd = (ChuyenDe) cbbTenChuyenDe.getSelectedItem();
        KhoaHoc kh = new KhoaHoc();
        kh.setMaCD(cd.getMaCD());
        kh.setHocPhi(Float.valueOf(txtHocPhi.getText()));
        kh.setThoiLuong(Integer.valueOf(txtThoiLuong.getText()));
        kh.setNgayKG(XDate.toDate(((JTextField) txtNgayKG.getDateEditor().getUiComponent()).getText(), "dd-MM-yyyy"));
        kh.setNgayTao(XDate.toDate(((JTextField) txtNgayTao.getDateEditor().getUiComponent()).getText(), "dd-MM-yyyy"));
        kh.setGhiChu(txtGhiChu.getText());
        kh.setMaNV(Auth.user.getMaNv());
        return kh;

    }

