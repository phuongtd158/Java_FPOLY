    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        Vector v = new Vector();
        v.add(ma.getText());
        v.add(ten.getText());
        v.add(dvt.getSelectedItem().toString());
        v.add(Double.parseDouble(gia.getText()));
        v.add(ncc.getText());
        model.addRow(v);
    }//GEN-LAST:event_themActionPerformed

    private void xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaActionPerformed
        model = (DefaultTableModel) tblShow.getModel();
        index = tblShow.getSelectedRow();
        model.removeRow(index);
    }//GEN-LAST:event_xoaActionPerformed

    private void suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaActionPerformed
        model.setValueAt(ma.getText().trim(), index, 0);
        model.setValueAt(ten.getText().trim(), index, 1);
        model.setValueAt(dvt.getSelectedItem(), index, 2);
        model.setValueAt(gia.getText().trim(), index, 3);
        model.setValueAt(ncc.getText().trim(), index, 4);
    }//GEN-LAST:event_suaActionPerformed

    private void tblShowMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblShowMouseClicked
        index = tblShow.getSelectedRow();
        ma.setText(model.getValueAt(index, 0).toString());
        ten.setText(model.getValueAt(index, 1).toString());
        dvt.setSelectedItem(model.getValueAt(index, 2).toString());
        gia.setText(model.getValueAt(index, 3).toString());
        ncc.setText(model.getValueAt(index, 4).toString());
    }//GEN-LAST:event_tblShowMouseClicked

