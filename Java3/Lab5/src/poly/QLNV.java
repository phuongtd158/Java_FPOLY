    private void loadDataToArray() {
        rdoNam.setSelected(true);
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection con = DriverManager.getConnection(url, user, pass);
            Statement stm = con.createStatement();
            String sql = "select * from STUDENTS";
            ResultSet rs = stm.executeQuery(sql);
            listSv.clear();
            while (rs.next()) {
                String maSv = rs.getString(1);
                String hoTen = rs.getString(2);
                String email = rs.getString(3);
                String diaChi = rs.getString(6);
                String sdt = rs.getString(4);
                boolean gioiTinh = rs.getBoolean(5);

                Student sv = new Student(maSv, hoTen, diaChi, email, sdt, gioiTinh);
                listSv.add(sv);
            }
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void showDetail() {
        Student sv = listSv.get(index);
        txtMaNV.setText(sv.maSv);
        txtDiaChi.setText(sv.getDiaChi());
        txtEmail.setText(sv.getEmail());
        txtHoTen.setText(sv.getHoTen());
        txtSDT.setText(sv.getSdt());
        if (sv.isGioiTinh() == true) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
    }

