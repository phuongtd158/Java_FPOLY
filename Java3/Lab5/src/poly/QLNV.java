    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String maNv = JOptionPane.showInputDialog(this, "Mời bạn nhập mã sinh viên muốn xóa");
        try {
            if (maNv.equals("")) {
                JOptionPane.showMessageDialog(this, "Hãy nhập mã");
                return;
            }
            if (checkTrung(maNv) == false) {
                JOptionPane.showMessageDialog(this, "Mã sinh viên không tồn tại");
                return;
            } else {
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection con = DriverManager.getConnection(url, user, pass);
                String sql = "delete from SINHVIEN where MASV = ?";
                PreparedStatement statement = con.prepareStatement(sql);
                statement.setString(1, maNv);
                statement.execute();

                con.close();
                loadDataToArray();
                JOptionPane.showMessageDialog(this, "Xóa thành công");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnXoaActionPerformed

