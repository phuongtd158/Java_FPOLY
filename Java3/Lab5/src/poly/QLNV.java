    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String maNv = JOptionPane.showInputDialog(this, "Mời nhập mã NV cần tìm");
        try {
            if (checkTrung(maNv) == false) {
                JOptionPane.showMessageDialog(this, "Không tìn thấy sv có mã: " + maNv);
                return;
            } else if (maNv.equals("")) {
                JOptionPane.showMessageDialog(this, "Không được để trống mã sv");
                return;
            } else {
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection con = DriverManager.getConnection(url, user, pass);
                String sql = "select * from STUDENTS where MASV = ?";
                PreparedStatement statement = con.prepareStatement(sql);

                statement.setString(1, maNv);

                statement.execute();
                ResultSet rs = statement.executeQuery(sql);
                String maSv = rs.getString(1);
                String tenSv = rs.getString(2);
                JOptionPane.showMessageDialog(this, "Đã tìn thấy sv có mã: " + maNv);
                JOptionPane.showMessageDialog(this, "Tên: " + tenSv);
                con.close();
                rs.close();
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButton1ActionPerformed

