    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        try {
            if (txtMaNV.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Không được để trống mã sinh viên");
                txtMaNV.requestFocus();
                return;
            } else if (checkTrung(txtMaNV.getText()) == true) {
                JOptionPane.showMessageDialog(this, "Mã sinh viên đã tồn tại");
                txtMaNV.requestFocus();
                return;
            } else {
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection con = DriverManager.getConnection(url, user, pass);
                String sql = "insert into STUDENTS(MASV,HOTEN, EMAIL, SDT, GIOITINH, DIACHI)\n"
                        + "values(?, ?, ?, ?, ?, ?)";
                PreparedStatement statement = con.prepareStatement(sql);

                statement.setString(1, txtMaNV.getText());
                statement.setString(2, txtHoTen.getText());
                statement.setString(3, txtEmail.getText());
                statement.setString(4, txtSDT.getText());
                boolean gt;
                if (rdoNam.isSelected()) {
                    gt = true;
                } else {
                    gt = false;
                }
                statement.setBoolean(5, gt);

                statement.setString(6, txtDiaChi.getText());

                statement.executeUpdate();
                JOptionPane.showMessageDialog(this, "Lưu thành công");
                con.close();

                loadDataToArray();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLuuActionPerformed
    private boolean checkTrung(String maSv) {
        boolean check = false;
        for (Student x : listSv) {
            if (x.getMaSv().equals(maSv)) {
                check = true;
            }
        }
        return check;
    }

