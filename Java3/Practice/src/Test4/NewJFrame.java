    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            cn = Prt_8.XHelper.ketNoi("QLSV");
            String sql = "select * from USERS";

            PreparedStatement stm = cn.prepareStatement(sql);
            ResultSet rs = stm.executeQuery();

            while (rs.next()) {
                if (rs.getString(1).equalsIgnoreCase(txtTaiKhaon.getText()) && rs.getString(2).equalsIgnoreCase(txtPass.getText())) {
                    JOptionPane.showMessageDialog(this, "Ok");
                    if (rs.getString(3).equalsIgnoreCase("GV")) {
                        NewJFrame1 form1 = new NewJFrame1();
                        form1.setVisible(true);

                    } else if (rs.getString(3).equalsIgnoreCase("DT")) {
                        JOptionPane.showMessageDialog(this, "Dt");
                        NewJFrame2 form2 = new NewJFrame2();
                        form2.setVisible(true);
                    }
                }
            }

            cn.close();
            stm.close();
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

