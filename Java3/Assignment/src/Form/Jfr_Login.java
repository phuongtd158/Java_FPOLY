    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (txtUsername.getText().equals("")) {
            DialogHelper.showErrorDialog(this, "Lỗi", "Không đươc để trống user");
            return;
        } else if (txtPass.getPassword().equals("")) {
            DialogHelper.showErrorDialog(this, "Lỗi", "Không đươc để trống pass");
            return;
        }
        NguoiDungDAO dao = new NguoiDungDAO();
        try {
            NguoiDung nd = dao.checkLogin(txtUsername.getText(), new String(txtPass.getPassword()));
            if (nd == null) {
                DialogHelper.showErrorDialog(this, "Lỗi", "User hoặc password sai");
                return;
            } else {
                SharedData.nguoiDangNhap = nd;
                this.dispose();
            }
        } catch (Exception e) {
            e.printStackTrace();
            DialogHelper.showErrorDialog(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

