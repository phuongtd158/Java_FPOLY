    private void btnValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidateActionPerformed
        String result = "";
        try {
            if (check()) {
                result = "Name: " + txtName.getText() + "\n";
                result = result + "Address: " + txtAdd.getText() + "\n";
                if (rdoMale.isSelected()) {
                    result = result + "Sex: Male\n";
                } else {
                    result = result + "Sex: Female\n";
                }
                result = result + "Quali: " + (String) cbbQuali.getSelectedItem() + "\n";
                if (chkReading.isSelected()) {
                    result = result.equals("") ? "Reading" : result + ", Reading";
                }
                if (chkSinging.isSelected()) {
                    result = result.equals("") ? "Singing" : result + ", Singing";
                }
                if (chkDancing.isSelected()) {
                    result = result.equals("") ? "Dancing" : result + ", Dancing";
                }
                
            }
        } catch (Exception e) {

        }
        JOptionPane.showMessageDialog(this, result);
    }//GEN-LAST:event_btnValidateActionPerformed
    public boolean check() {
        if (txtName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để tên trống");
            return false;
        } else if (txtAdd.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để địa chỉ trống");
            return false;
        }
        return true;

    }

