    private void btnSignUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignUpActionPerformed
        try {
            if (txtUser.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Không được để trống tên tài khoản");
                txtUser.requestFocus();
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi" + e);
            return;
        }

        try {
            if (txtPass.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Không được để trống mật khẩu");
                txtPass.requestFocus();
                return;
            }
            if (txtConfirmpass.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Không được để trống mật khẩu");
                txtConfirmpass.requestFocus();
                return;
            } else if (!(txtPass.getText()).equals(txtConfirmpass.getText())) {
                JOptionPane.showMessageDialog(this, "Mật khẩu không trùng nhau vui lòng nhập lại");
                return;
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi" + e);
            return;
        }
        JOptionPane.showMessageDialog(this, "Đăng ký thành công");

    }//GEN-LAST:event_btnSignUpActionPerformed

