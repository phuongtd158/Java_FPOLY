    private void time() {
        t = new Thread() {
            @Override
            public void run() {
                int mins = Integer.valueOf(lbMin.getText());
                int secs = Integer.valueOf(lbSec.getText());
                while (secs >= 0 && mins >= 0) {

                    secs--;
                    lbSec.setText(String.valueOf(secs));

                    if (secs == 0) { //Nếu giây = 0
                        secs = 60;   //Thì giây = 60
                        if (mins == 0) { //Nếu cả phút và giây điều bằng 0 thì kết thúc
                            break;
                        }
                        mins--; //Mỗi lần giây = 0 thì phút sẽ trừ đi 1
                        lbMin.setText(String.valueOf(mins));
                    }

                    try {
                        Thread.sleep(1000);
                    } catch (Exception e) {
                        break;
                    }
                }

            }

        };

        t.start();
    }

